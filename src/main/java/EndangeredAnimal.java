public class EndangeredAnimal  {
    public String name;
    public int id;
    public boolean endangered;
    private String health;
    private String age;


    public EndangeredAnimal(String name){
        this.name=name;
        this.id=id;

        this.health=health;
        this.age=age;

    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
    public String getHealth() {
        return health;
    }

    public String getAge() {
        return age;
    }
}
